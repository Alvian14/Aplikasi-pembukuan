     private void idotomatis(){
           try{
            Connection c = Koneksi.configDB();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM akun ORDER BY id_akun DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String NoFaktur = r.getString("id_akun").substring(2);
                String B = "" +(Integer.parseInt(NoFaktur)+1);
                String Nol = "";
                
                if(B.length()==1)
                {Nol = "000";}
                else if(B.length()==2)
                {Nol = "00";}
                else if(B.length()==3)
                {Nol = "0";}
                else if(B.length()==4)
                {Nol = "";}
                txtidAkun.setText("A" + Nol + B);
            }else{      
                txtidAkun.setText("A0001");
            } 
            r.close();
            s.close();  
        }catch (Exception e) { 
            System.out.println("autonumber eror");
        }
       }
     
