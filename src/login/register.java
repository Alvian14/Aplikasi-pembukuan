    private void btnDaftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDaftarActionPerformed
     if(txtNama.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nama Harus Di isi");
        }else if(txtUsername.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Username Harus Di isi");
        } else if (txtPass.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Password Harus Di isi");
        } else if (date.getDate().equals("")){
            JOptionPane.showMessageDialog(null, "Tanggal Lahir Harus Di isi");
        } else if (txtgender.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Jenis Kelamin Harus Di isi");
        } else if (txtNotlpn.getText().equals("")){
            JOptionPane.showMessageDialog(null, "No Hp Harus Di isi");      
        } else if  (txtAlamat.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Alamat Harus Di isi");
        } else if (this.txtNotlpn.getText().length()<12){
            JOptionPane.showMessageDialog(this, "No Telpn min 12 digit");
        } else if (this.txtNotlpn.getText().length()>13){
            JOptionPane.showMessageDialog(this, "No Telpn max 13 digit");
        }else if (this.txtPass.getText().length()<6){  
            JOptionPane.showMessageDialog(this,"Password min 6 karakter");
        }else if (this.txtPass.getText().length()>10){
            JOptionPane.showMessageDialog(null,"Password max 10 karakter");
        }else{    
            
       
        
     
         String tanggalahir = "yyyy-MM-dd";
         SimpleDateFormat fm = new SimpleDateFormat(tanggalahir);
         String tanggal = String.valueOf(fm.format(date.getDate()));
         String sql = "insert into akun values (?,?,?,?,?,?,?,?)";
         
         
         try {
            java.sql.Connection conn=(Connection) Koneksi.configDB();
            java.sql.PreparedStatement stat=conn.prepareStatement(sql);
            stat.setString(1, txtidAkun.getText());
            stat.setString(2, txtNama.getText());
            stat.setString(3, txtUsername.getText ());
            stat.setString(4, txtPass.getText());
            stat.setString(5, tanggal);
            stat.setString(6,txtgender.getText());
            stat.setString(7, txtNotlpn.getText());
            stat.setString(8, txtAlamat.getText());
            if (JOptionPane.showConfirmDialog(null, "Apakah data yang dimasukan telah sesuai?", "WARNING",
            JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {     
                 stat.executeUpdate();
                 JOptionPane.showMessageDialog(null, "Akun Berhasil Dibuat");
                 kosong();
                 this.setVisible(false);
                 new Login().setVisible(true);
            } else {
        }
       txtNama.requestFocus(); 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Akun Gagal Dibuat"+e);
        }
         idotomatis();
     }
         
         
    }//GEN-LAST:event_btnDaftarActionPerformed

