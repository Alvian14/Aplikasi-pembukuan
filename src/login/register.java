    private void txtPassKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPassKeyReleased
        // untuk indikator password 
        String password=this.txtPass.getText();
        char[] chars = password.toCharArray();
        hasnumber ="";
        for(char c: chars ){
            if(Character.isDigit(c)){
            hasnumber = "yes";
        }
        }
        
        if(password.length() > 10 &&!password.equals(password.toLowerCase())&&hasnumber.equals("yes")){
            this.indikator.setText("KUAT");
            indikator.setForeground(Color.pink);
        }else if(this.txtPass.getText().length()>8){
            this.indikator.setText("KUAT");
            indikator.setForeground(Color.GREEN);
        }else if(this.txtPass.getText().length()>6){
            this.indikator.setText("SEDANG");
            indikator.setForeground(Color.blue);
        }else{
            this.indikator.setText("LEMAH");
            indikator.setForeground(Color.red);
        }
    }//GEN-LAST:event_txtPassKeyReleased

