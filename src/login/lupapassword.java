    private void btnLanjutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLanjutActionPerformed
         if (txtUser.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Username Harus Di isi");
        } else if (txtPass.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Password Baru Harus Di isi");
        } else if (txtNohp.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Nomer Kontak Harus Di isi");
        } else {

            if (this.cekUserKontak()) {
                String sql = "UPDATE akun Set password = '" + txtPass.getText() + "' WHERE username = '" 
                        + txtUser.getText() + "'";

                    
                try {
                    Connection c = (Connection) Koneksi.configDB();
                    Statement s = c.createStatement();

                    if (JOptionPane.showConfirmDialog(null, "Apakah data yang dimasukan telah sesuai?", "WARNING",
                            JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                        int res = s.executeUpdate(sql);
                        JOptionPane.showMessageDialog(null, "Password Berhasil Diganti");
                        this.setVisible(false);
                        new Login().setVisible(true);
                        
                        
                        
                    }
                 
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Password Gagal Diganti"+e);
                        
                    
                }
                    }else
                     JOptionPane.showMessageDialog(null, "Password Gagal Diganti");
            {
                
            
            }
        }
                    
                       

                    
    }//GEN-LAST:event_btnLanjutActionPerformed

