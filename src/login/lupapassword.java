     private boolean cekUserKontak() {
        try {
            String sql = "SELECT username, no_telp FROM akun WHERE username = '" + txtUser.getText() + "' AND no_telp = '" + txtNohp.getText() + "'";
            Connection c = (Connection) Koneksi.configDB();
            Statement s = c.createStatement();
            ResultSet r = s.executeQuery(sql);

            if (r.next()) {
                return true;
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        return false;
    }
            
            
     
